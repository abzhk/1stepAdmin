import mongoose from "mongoose";

const assessmentCategorySchema = new mongoose.Schema(
  {
    name: {
      type: String,
      required: true,
      unique: true,
      trim: true,
    },
    description: {
      type: String,
      required: true,
    },
    icon: {
      type: String,
      default: null,
    },
    status: {
      type: Boolean,
      default: true,
    },
    order: {
      type: Number,
      default: 0,
    },
  },
  { timestamps: true }
);

const assessmentCategory = mongoose.model(
  "assessmentCategory",
  assessmentCategorySchema
);
export default assessmentCategory;
